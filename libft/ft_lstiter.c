#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*ptr;

	if (!lst || !f)
		return ;
	while (lst != NULL)
	{
		ptr = lst->next;
		(*f)(lst->content);
		lst = ptr;
	}
	lst = NULL;
}
